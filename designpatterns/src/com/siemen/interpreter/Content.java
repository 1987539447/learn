package com.siemen.interpreter;

public class Content {

	private int numA;
	private int numB;

	public Content(int numA, int numB) {
		this.numA = numA;
		this.numB = numB;
	}

	/**
	 * @return the numA
	 */
	public int getNumA() {
		return numA;
	}

	/**
	 * @param numA the numA to set
	 */
	public void setNumA(int numA) {
		this.numA = numA;
	}

	/**
	 * @return the numB
	 */
	public int getNumB() {
		return numB;
	}

	/**
	 * @param numB the numB to set
	 */
	public void setNumB(int numB) {
		this.numB = numB;
	}

}
